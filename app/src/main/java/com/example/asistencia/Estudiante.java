package com.example.asistencia;

public class Estudiante {

    String nombre;
    String telefono;
    String cedula;
    String correo;

    public Estudiante(String nombre, String telefono, String cedula, String correo){
        this.nombre = nombre;
        this.telefono = telefono;
        this.cedula = cedula;
        this.correo = correo;
    }

}